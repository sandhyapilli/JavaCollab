package programs;


	public class Employee {
	    private String name;
	    private double salary;
	    private int id;

	    // Constructor, getters, and setters
	    public Employee(String name, double salary, int id) {
	        this.name = name;
	        this.salary = salary;
	        this.id = id;
	    }

	    public double getSalary() {
	        return salary;
	    }
	}

	


